 package StringProgram;
//How to create a String.
public class SP1 {
	public static void main(String[] args) {
		//Using String Literal
		String s1="bye";
		String s2="bye";
		System.out.println(s1==s2);
		
		//Using New Keyword
		String s3=new String("bye");
		String s4=new String("bye");
		System.out.println(s3==s4);

	}

}
